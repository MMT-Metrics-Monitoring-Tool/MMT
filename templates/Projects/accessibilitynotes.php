<h2>Notes for screen reader users</h2>
<h3>Writing dates</h3>

<p>In many parts of this site, you have to write dates. This is done as follows:
<ol>
<li>Go to the date form field.</li>
<li>If you want to use the current date, activate your screen reader's forms mode if it is not active yet and press enter, which puts the date to the field.</li>
<li>If you want to use some other date, go to the graphic where screen reader says "select date" and press enter. This opens a calendar table  that appears below the submit button. Select the current date by pressing enter, which puts the date to the input field.</li>
</ol>
</p>
<p>Note: Sometimes the date doesn't go to the field although you choose it to the calendar. If this happens, update the page before retrying. This happens especially when logging some other user's data. To avoid this, select the date before selecting the user whose time you are logging.</p>
<h3>About the charts</h3>
<p>The charts themselves aren't accessible. You can still see your and other project members' logged hours and how many hours there are in each work type. This information is in the members section. Do the following:
<ol>
<li>Select the project whose hours you want to observe.</li>
<li>Go to the Members section of the project.</li>
<li>In this page, the first table shows the working hour amount of each project member.</li>
<li>In the second table of the page, there are  the hours and percentage of each work type from the whole group.</li>
</ol>
If you want to observe the working hours of a single member, including yourself, open the member's page. The hours are below the corresponding heading, and there are shown how much time the selected member has  logged into each work type.
</p>
